                <input class="rounded-full mt-1 bg-zinc-900 px-3" type="text" id="name" name="name" required>
            </div>
            <div class="flex flex-col max-w-[330px]">
                <label class="ml-1" for="worked_at">Onde você trabalhou ultimamente?</label>
                <div>
                    <input class="rounded-full mt-1 bg-zinc-900 px-3" type="text" id="worked_at" name="worked_at">
                    <button type="button" id="add-xp-btn" class="px-2"><i class="bi bi-plus-circle"></i></button>
                </div>
            </div>
            <div id="xp-added-area" class="mt-2 gap-1 flex flex-wrap max-w-[300px]">
                <!-- Generated by <script> -->
            </div>

            <button type="submit" class="bg-emerald-400 text-zinc-950 font-[600] tracking-tight rounded-full text-xl py-1 px-4">Enviar</button>

            <div id="response-message" class="mt-2"></div>
        </form>
