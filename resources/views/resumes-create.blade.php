    <script>
        const addxpbtn = document.getElementById("add-xp-btn")
        const workedatinput = document.getElementById("worked_at")
        const xpArray = []
        const xpAddedArea =  document.getElementById("xp-added-area") 

        function updateXpAddedArea() {
            xpAddedArea.innerHTML = ""
            xpArray.forEach(xp => {
                const xpDivToAppend = document.createElement("div")
                xpDivToAppend.classList.add('px-6', 'py-1', 'rounded-full', 'border', 'border-emerald-600')
                xpDivToAppend.innerText = xp + " "
                
                const i = document.createElement("i")
                i.classList.add("bi", "bi-x-circle", "text-red-500")
                xpDivToAppend.append(i)
                
                xpDivToAppend.addEventListener('click', () => {
                    xpArray.splice(xpArray.indexOf(xp), 1)
                    updateXpAddedArea()
                })
                
                xpAddedArea.append(xpDivToAppend)
            })
        }

        addxpbtn.addEventListener('click', (e) => {
            e.preventDefault()
            xpArray.push(workedatinput.value)
            updateXpAddedArea()
            workedatinput.value = ""
            workedatinput.focus()
        })

        document.getElementById('add-resume-form').addEventListener('submit', async function(event) {
            event.preventDefault();

            const formData = {
                'name': document.getElementById('name').value,
                'worked_at': xpArray,
            };

            try {
                let response = await fetch("{{ url('/api/resume/create') }}", {
                    method: "POST",
                    headers: {
                        "Content-Type": "application/json",
                        "Accept": "application/json",
                        "X-CSRF-TOKEN": "{{ csrf_token() }}"
                    },
                    body: JSON.stringify(formData)
                });

                let result = await response.json();

                if (response.ok) {
                    document.getElementById('response-message').innerHTML = `<p style="color:green;">${result.message}</p>`;
                } else {
                    document.getElementById('response-message').innerHTML = `<p style="color:red;">${JSON.stringify(result.errors)}</p>`;
                }
            } catch (error) {
                console.error("Erro:", error);
            }
        });
    </script>
